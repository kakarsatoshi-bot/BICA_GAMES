/* 図鑑に載る全モンスターのリスト（シェイプ・パレット付き）を組み立てる。
 * ALL_QUESTS がまだ定義されていない読み込み順でも呼べるよう、game.js から呼び出して使う。 */
function buildBestiaryList(allQuests) {
  var list = [];
  var seen = {};
  function add(m, rare) {
    if (seen[m.name]) return;
    seen[m.name] = true;
    list.push({ name: m.name, shape: m.shape, palette: m.palette, rare: !!rare });
  }
  allQuests.forEach(function (q) {
    add(q.mob);
    add(q.boss);
  });
  add({ name: "まおうデリート", shape: "demon", palette: "dark" });
  add(RARE_MONSTER, true);
  return list;
}
