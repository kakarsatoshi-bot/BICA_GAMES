/* ---- 描画 ---- */
function drawSprite(canvas, shapeKey, paletteKey, scale) {
  var rows = MONSTER_SHAPES[shapeKey] || FX_SHAPES[shapeKey];
  var pal = PIXEL_PALETTES[paletteKey] || PIXEL_PALETTES.green;
  var w = rows[0].length, h = rows.length;
  canvas.width = w * scale;
  canvas.height = h * scale;
  var ctx = canvas.getContext("2d");
  ctx.imageSmoothingEnabled = false;
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  for (var y = 0; y < h; y++) {
    for (var x = 0; x < rows[y].length; x++) {
      var c = rows[y][x];
      if (c === ".") continue;
      var color;
      if (c === "W") color = SPRITE_WHITE;
      else if (c === "B") color = SPRITE_BLACK;
      else color = pal[c] || pal[1];
      ctx.fillStyle = color;
      ctx.fillRect(x * scale, y * scale, scale, scale);
    }
  }
}
